// 1. Create a variable called `multiply` set to an equation that will multiply
//    the variables `num1` and `num2`; the result of the multiplication should be `62`.
const num1 = 31; // Example value
const num2 = 2;  // Example value
const multiply = num1 * num2; // This will be 62
console.log("Multiply:", multiply); // Output: Multiply: 62

// 2. Create a variable called `random` that will generate a random integer greater than 0.
const random = Math.floor(Math.random() * 100) + 1; // Generates a random integer between 1 and 100
console.log("Random:", random);

// 3. Create a variable called `mod` set to an equation that will calculate
//    the remainder of dividing variable `num3` by `num4`; the remainder should be `4`.
const num3 = 24; // Example value
const num4 = 10; // Example value
const mod = num3 % num4; // This will be 4
console.log("Mod:", mod); // Output: Mod: 4

// 4. Create a variable called `max` that finds the highest number in a set; 
//    the value returned should be `20`.
const numbers = [10, 15, 20, 5]; // Example array
const max = Math.max(...numbers); // This will find the maximum value
console.log("Max:", max); // Output: Max: 20
